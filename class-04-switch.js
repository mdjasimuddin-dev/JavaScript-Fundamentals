/* JS switch statement */
let grade = "C";

switch(grade){
    case "A":
    console.log("A grade");
    break;

    case "B":
        console.log("B grade");
    break;


    case "C":
        console.log("C grade");
    break;


    case "D":
        console.log("D grade")
    break;


    case "E":
        console.log("E grade")
    break;
}